package com.koreait.first;

public class CountriesVO {
	
	private String contury_id;
	private String contury_name;
	private int region_id;
	
	
	
	public String getContury_id() {
		return contury_id;
	}
	public void setContury_id(String contury_id) {
		this.contury_id = contury_id;
	}
	public String getContury_name() {
		return contury_name;
	}
	public void setContury_name(String contury_name) {
		this.contury_name = contury_name;
	}
	public int getRegion_id() {
		return region_id;
	}
	public void setRegion_id(int region_id) {
		this.region_id = region_id;
	}
	
	

}
